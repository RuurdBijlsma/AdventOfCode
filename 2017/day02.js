sheet = input.split('\n')
    .map(row => row
        .split('\t')
        .map(n => +n)
    );


checksum = 0;
for (let row of sheet) {
    max = Math.max(...row);
    min = Math.min(...row);
    checksum += max - min;
}
checksum;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////// PART 2 //////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

sheet = input.split('\n')
    .map(row => row
        .split('\t')
        .map(n => +n)
    );


sum = 0;
for(let row of sheet){
	for(let i = 0; i < row.length - 1; i++){
		value = row[i];
		for(let j = i + 1; j < row.length; j++){
            otherValue = row[j];
            if(value % otherValue === 0)
				sum += value / otherValue;
            if(otherValue % value === 0)
				sum += otherValue / value;
        }
    }
}
sum;